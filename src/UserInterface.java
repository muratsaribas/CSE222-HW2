public interface UserInterface {
    /**
     * Get user's name
     * @return user's name
     */
    String getName();

    /**
     * Get user's surname
     * @return user's surname
     */
    String getSurname();

    /**
     * Get user's email
     * @return user's email
     */
    String getEmail();

    /**
     * Get user's password
     * @return user's password
     */
    String getPassword();

    /**
     * Get user's id number
     * @return user id
     */
    int getUserID();


}
